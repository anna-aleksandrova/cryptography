    def __init__(self, key: bytes):
        self.key = list(key)
        self.Nk = len(key) // 4           # number of words in the key
        self.Nb = 4                       # number of words in the block

        if self.Nk == 4:
            self.Nr = 10                  # number of rounds
        elif self.Nk == 6:
            self.Nr = 12
        elif self.Nk == 8:
            self.Nr = 14
        else:
            raise ValueError("Invalid key size")
        
        self.round_keys = self._key_expansion()

    # multiplication in GF(2^8) = Z2[x] / (x^8 + x^4 + x^3 + x + 1)
    def _gmult(self, a, b):  
        p = 0
        for _ in range(8):
            if b & 1: 
                p ^= a
            hi_bit_set = a & 0x80
            a = (a << 1) & 0xFF
            if hi_bit_set:
                a ^= 0x1b
            b >>= 1  
        return p

    # round functions
    def SubBytes(self, state):
        for i in range(16):
            state[i] = self.SBOX[state[i]]
    
    def InvSubBytes(self, state):
        for i in range(16):
            state[i] = self.INV_SBOX[state[i]]

    def ShiftRows(self, state):
        state[1], state[5], state[9], state[13] = state[5], state[9], state[13], state[1]
        state[2], state[6], state[10], state[14] = state[10], state[14], state[2], state[6]
        state[3], state[7], state[11], state[15] = state[15], state[3], state[7], state[11]

    def InvShiftRows(self, state):
        state[1], state[5], state[9], state[13] = state[13], state[1], state[5], state[9]
        state[2], state[6], state[10], state[14] = state[10], state[14], state[2], state[6]
        state[3], state[7], state[11], state[15] = state[7], state[11], state[15], state[3]
    
    def MixColumns(self, state):
        for i in range(4):
            col = i * 4
            a = state[col:col+4]
            state[col]   = self._gmult(0x02, a[0]) ^ self._gmult(0x03, a[1]) ^ a[2] ^ a[3]
            state[col+1] = a[0] ^ self._gmult(0x02, a[1]) ^ self._gmult(0x03, a[2]) ^ a[3]
            state[col+2] = a[0] ^ a[1] ^ self._gmult(0x02, a[2]) ^ self._gmult(0x03, a[3])
            state[col+3] = self._gmult(0x03, a[0]) ^ a[1] ^ a[2] ^ self._gmult(0x02, a[3])
    
    def InvMixColumns(self, state):
        for i in range(4):
            col = i * 4
            a = state[col:col+4]
            state[col]   = self._gmult(0x0e, a[0]) ^ self._gmult(0x0b, a[1]) ^ self._gmult(0x0d, a[2]) ^ self._gmult(0x09, a[3])
            state[col+1] = self._gmult(0x09, a[0]) ^ self._gmult(0x0e, a[1]) ^ self._gmult(0x0b, a[2]) ^ self._gmult(0x0d, a[3])
            state[col+2] = self._gmult(0x0d, a[0]) ^ self._gmult(0x09, a[1]) ^ self._gmult(0x0e, a[2]) ^ self._gmult(0x0b, a[3])
            state[col+3] = self._gmult(0x0b, a[0]) ^ self._gmult(0x0d, a[1]) ^ self._gmult(0x09, a[2]) ^ self._gmult(0x0e, a[3])
    
    def AddRoundKey(self, state, round_num):
        for i in range(16):
            state[i] ^= self.round_keys[round_num * 16 + i]
    
    # key expansion
    def _key_expansion(self):
        key_schedule = self.key[:]
        
        for i in range(self.Nk, self.Nb * (self.Nr + 1)):
            temp = key_schedule[(i - 1) * 4 : i * 4]
            
            if i % self.Nk == 0:
                # RotWord
                temp = temp[1:] + temp[:1]
                # SubWord
                temp = [self.SBOX[b] for b in temp]
                # XOR and Rcon
                rcon_val = self.RCON[i // self.Nk]
                temp = [temp[j] ^ rcon_val[j] for j in range(4)]
            elif self.Nk > 6 and i % self.Nk == 4:
                # Additional SubWord for AES-256
                temp = [self.SBOX[b] for b in temp]
                
            prev_word = key_schedule[(i - self.Nk) * 4 : (i - self.Nk + 1) * 4]
            new_word = [prev_word[j] ^ temp[j] for j in range(4)]
            key_schedule.extend(new_word)
            
        return key_schedule

    def encrypt(self, plaintext: bytes):
        assert len(plaintext) == 16
        state = list(plaintext)

        self.AddRoundKey(state, 0)

        for i in range(1, self.Nr):
            self.SubBytes(state)
            self.ShiftRows(state)
            self.MixColumns(state)
            self.AddRoundKey(state, i)

        self.SubBytes(state)
        self.ShiftRows(state)
        self.AddRoundKey(state, self.Nr)

        return bytes(state)

    def decrypt(self, ciphertext: bytes) -> bytes:
        assert len(ciphertext) == 16
        state = list(ciphertext)

        self.AddRoundKey(state, self.Nr)

        for i in range(self.Nr - 1, 0, -1):
            self.InvShiftRows(state)
            self.InvSubBytes(state)
            self.AddRoundKey(state, i)
            self.InvMixColumns(state)

        self.InvShiftRows(state)
        self.InvSubBytes(state)
        self.AddRoundKey(state, 0)

        return bytes(state)


